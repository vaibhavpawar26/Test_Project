package com.java.strings;

public class DemoLoopBrealk {

	public static void main(String[] args) {
		 for (int i = 1; i <= 10; i++) {
	            System.out.println("Number " + i);

	            if (i == 5) {
	                System.out.println("Breaking the loop at Number 5.");
	                break; // Exit the loop
	}

}
}
}